#include "gameover.h"
#include "gameoverBG.h"
#include "input.h"
#include "fade.h"
bool swgo = true;
//=============================================================================
// 初期化処理
//=============================================================================
void InitGameover(void)
{
	//リザルト背景管理の初期化処理
	InitGameoverBG();
	swgo = true;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitGameover(void)
{
	//リザルト背景管理の終了処理
	UninitGameoverBG();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateGameover(void)
{
	//リザルト背景管理の更新処理
	UpdateGameoverBG();

	if (GetKeyboardTrigger(DIK_RETURN) && swgo)
	{
		swgo = false;
		SetFade(FADE_OUT);
	}
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawGameover(void)
{
	//リザルト背景管理の描画処理
	DrawGameoverBG();
}
