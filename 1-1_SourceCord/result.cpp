#include "result.h"
#include "resultBG.h"
#include "input.h"
#include "fade.h"


bool swr = true;
//=============================================================================
// 初期化処理
//=============================================================================
void InitResult(void)
{
	//リザルト背景管理の初期化処理
	InitResultBG();
	swr = true;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitResult(void)
{
	//リザルト背景管理の終了処理
	UninitResultBG();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateResult(void)
{
	//リザルト背景管理の更新処理
	UpdateResultBG();

	if (GetKeyboardTrigger(DIK_RETURN) && swr)
	{
		swr = false;
		SetFade(FADE_OUT);
	}
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawResult(void)
{
	//リザルト背景管理の描画処理
	DrawResultBG();
}
